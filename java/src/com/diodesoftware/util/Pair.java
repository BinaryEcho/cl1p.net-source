package com.diodesoftware.util;

public class Pair {
	public String s;
	public int v;
	public Pair(String s, int v){
		this.s = s;
		this.v = v;
	}


    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }
}
